package day1PracticeProblems;

public class Default_Values_2 {
	/**
	 * initializing static values
	 * and printing its default values
	 */

		static int one;
		static String two;
		static boolean three;
		static byte four;
		static short five;
		static long six;
		static float seven;
		static double eight;
		static char nine;
		
		public static void main(String[] args) {

		System.out.println("The default value of all primitive data types in java are ");
		System.out.println("Int "+ one);
		System.out.println("String "+ two);
		System.out.println("Boolean "+three);
		System.out.println("Byte "+four);
		System.out.println("Short "+five);
		System.out.println("Long "+six);
		System.out.println("Float"+seven);
		System.out.println("Double "+eight);
		System.out.println("Char "+nine);

	}

}
